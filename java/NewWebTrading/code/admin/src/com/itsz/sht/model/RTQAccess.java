package com.itsz.sht.model;

public class RTQAccess {
	
	private String clientId;
	private String productId;
	private String rtqProvider;
	private String rtqUrl;
	private String Status;
	private String rtqLoginId;
	private String rtqLoginPassword;
	public String getClientId() {
		return clientId;
	}
	public void setClientId(String clientId) {
		this.clientId = clientId;
	}
	public String getProductId() {
		return productId;
	}
	public void setProductId(String productId) {
		this.productId = productId;
	}
	public String getRtqProvider() {
		return rtqProvider;
	}
	public void setRtqProvider(String rtqProvider) {
		this.rtqProvider = rtqProvider;
	}
	public String getRtqUrl() {
		return rtqUrl;
	}
	public void setRtqUrl(String rtqUrl) {
		this.rtqUrl = rtqUrl;
	}
	public String getStatus() {
		return Status;
	}
	public void setStatus(String status) {
		Status = status;
	}
	public String getRtqLoginId() {
		return rtqLoginId;
	}
	public void setRtqLoginId(String rtqLoginId) {
		this.rtqLoginId = rtqLoginId;
	}
	public String getRtqLoginPassword() {
		return rtqLoginPassword;
	}
	public void setRtqLoginPassword(String rtqLoginPassword) {
		this.rtqLoginPassword = rtqLoginPassword;
	}
	
	

}
